    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
       
        String song = songTf.getText();           
        String genre = (String) genreCb.getSelectedItem();

        // Initialize a variable to hold search results
        String result = "The song is not found in the playlist.";

        // Search songs in the appropriate stack based on the selected genre
        if (genre.equals("POP")) {
            // Search for songs in the pop genre
            result = popStack.search(song);
        } else if (genre.equals("ROCK")) {
            //  Search for songs in the rock genre
            result = rockStack.search(song);
        }

        // Display the result on the text area
        listTa.append(result + "\n"); 
 
    }//GEN-LAST:event_searchBtnActionPerformed

    private void nosBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nosBtnActionPerformed
        // TODO add your handling code here:
 
        // Get the number of songs in the pop, rock stack
        int numOfPopSongs = popStack.size();
        int numOfRockSongs = rockStack.size();
        int totalNumOfSongs = numOfPopSongs + numOfRockSongs;
        
        //Display the total num of songs
        listTa.append("There are " + totalNumOfSongs + " songs in the liked list.\n");
        
    }//GEN-LAST:event_nosBtnActionPerformed

