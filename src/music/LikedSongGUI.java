    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        
        if (songTf.getText().isEmpty()) {
            // Show an error message 
            JOptionPane.showMessageDialog(null, "Please enter the title of the song you wish to add.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
       
        String song = songTf.getText();           
        String genre = (String) genreCb.getSelectedItem();
        
        //Add songs into each stacks
            if (genre.equals("POP")) {
            popStack.push(song);
        } else if (genre.equals("ROCK")) {
            rockStack.push(song);
        }

        //Display added songs
        listTa.append(song + ", genre:" + genre+"\n");
        
        
    }//GEN-LAST:event_addBtnActionPerformed

    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        // TODO add your handling code here:
        
        // Display a message when the stack is empty
        if (popStack.isEmpty() && rockStack.isEmpty()) {
        listTa.append("There are no songs to be removed!\n");
    } else {
        // Remove rock songs after removing all pop songs
        String removedSong = null;
        if (!popStack.isEmpty()) {
            removedSong = (String) popStack.pop();
        } else if (!rockStack.isEmpty()) {
            removedSong = (String) rockStack.pop();
        }
        if (removedSong != null) {
            listTa.append(removedSong + " was removed from the list.\n");
        }
    }
        
    }//GEN-LAST:event_removeBtnActionPerformed

